    private void jButtonCadastrarEmpresaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarEmpresaActionPerformed
        Empresa empresa = new Empresa();
        empresa.setNomeEmpresa(jTextFieldNomeEmpresa.getText());
        
        if(jTextFieldNomeEmpresa.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Os campos n√£o podem retornar vazios");
        } else {
            EmpresaDAO dao = new EmpresaDAO();
            dao.adiciona(empresa);
            JOptionPane.showMessageDialog(null, "Empresa "+jTextFieldNomeEmpresa.getText() + " cadastrado com sucesso!");
        }
        
        jTextFieldNomeEmpresa.setText("");
    }//GEN-LAST:event_jButtonCadastrarEmpresaActionPerformed

