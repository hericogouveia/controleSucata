    private void jButtonCadastrarTipoMaterialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarTipoMaterialActionPerformed
        TipoMaterial tipoMaterial = new TipoMaterial();
        tipoMaterial.setTipoMaterial(jTextFieldTipoMaterial.getText());
        
        if(jTextFieldTipoMaterial.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Os campos n√£o podem retornar vazios");
        } else {
            TipoMaterialDAO dao = new TipoMaterialDAO();
            dao.adiciona(tipoMaterial);
            JOptionPane.showMessageDialog(null, "Tipo Material "+jTextFieldTipoMaterial.getText() + " cadastrado com sucesso!");
        }
        
        jTextFieldTipoMaterial.setText("");
    }//GEN-LAST:event_jButtonCadastrarTipoMaterialActionPerformed

